<h2 class="about"> Our Method </h2>
<div class="aboutInfo"> At Market Finder, we aim to better target the advertising for a marketing companies through
    analysing the frequency and preference of products mentioned on social platforms, such as Flickr and Twitter. Our
    application first identifies the geographical areas where sales are below average. Here, we introduce a heat map for
    illustrating the geographical location of specific photos on Flickr and specific tweets on Twitter, as well as a pie
    chart for representing the total number of tweets mentioning our product and the competitors'. In this manner, we
    could evidently discover the market shares of the products, potentially assisting to search the focus of
    advertising. We next assess the public's affection of our products utilizing VADER. This sentiment analysis tool
    scores text emotion by searching text keywords based on lexicon and grammar rules. Lastly and most importantly, our
    products would be further applied in practice. On the one hand, the potential customers can be found based on their
    interests, followed by establishing their profile including their gender, age, hobbies, and geographical locations.
    On the other hand, the most-watched TV programs from Twitter and the most visited places from Flickr could be sought
    out, providing valuable suggestions for marketing company to advertise the products. </div>
<h2 class="about">Our Goals </h2>
<div class="aboutInfo">Our objective is to analyse where, when and how a given product is mentioned on social media so
    that our clients can better target their advertising efforts. To aid in this effort not only do we provide
    geographical locations of where sales are below average but, using our software we can build a profile of potential
    customers and discover details on their most popular TV programmes. This will allow our clients to pick commercial
    breaks of which TV programs to advertise the product, or at which locations to pay for outdoor advertising. </div>
